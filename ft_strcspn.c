#include <stdio.h>

size_t	ft_strcspn(const char *s, const char *reject)
{
	size_t i = 0;
	int j;

	while (s[i])
	{
		j = 0;
		while (reject[j])
		{
			if (s[i] == reject[j])
				return (i);
			j++;
		}
		i++;
	}
	return (i);
}

int main()
{
	const char *s = "Hello";
	const char *reject = "lo";
	printf("%zu", ft_strcspn(s, reject));
	return (0);
}